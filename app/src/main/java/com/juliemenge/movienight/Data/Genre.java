package com.juliemenge.movienight.Data;

public class Genre {
    private int id; //genre id, like 28
    private String name; //genre name, like Action

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
